#!/usr/bin/env node
/**
 * Rebuild the standalone, self-contained data-map HTML by inlining
 * cesafiu_prototype_v3/project/data.js into cesafiu-data-map.html.
 *
 * Usage: node scripts/build-data-map.js
 *
 * Output: cesafiu-data-map-standalone.html (sibling of cesafiu-data-map.html)
 *
 * Run this whenever data.js or the map template is updated.
 */
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const TEMPLATE = path.join(repoRoot, 'cesafiu-data-map.html');
const DATA = path.join(repoRoot, 'cesafiu_prototype_v3/project/data.js');
const OUT = path.join(repoRoot, 'cesafiu-data-map-standalone.html');

if (!fs.existsSync(TEMPLATE)) { console.error('Missing:', TEMPLATE); process.exit(1); }
if (!fs.existsSync(DATA)) { console.error('Missing:', DATA); process.exit(1); }

const html = fs.readFileSync(TEMPLATE, 'utf8');
const dataJs = fs.readFileSync(DATA, 'utf8');

const tag = '<script src="cesafiu_prototype_v3/project/data.js"></script>';
const inline = `<script>\n/* === Inlined from cesafiu_prototype_v3/project/data.js (auto-generated by scripts/build-data-map.js — do not edit) === */\n${dataJs}\n</script>`;

if (!html.includes(tag)) {
  console.error('Could not find script tag to replace. Template may have been edited.');
  process.exit(1);
}

const out = html
  .replace(tag, inline)
  .replace(
    'data: cesafiu_prototype_v3/project/data.js · v2 expansion 2026-04-30',
    `data: inlined standalone · built ${new Date().toISOString().slice(0, 10)}`
  );

fs.writeFileSync(OUT, out, 'utf8');
const sizeKB = (fs.statSync(OUT).size / 1024).toFixed(1);
console.log(`Built ${path.relative(repoRoot, OUT)} (${sizeKB} KB)`);
